curso = "  pYtHon  "

#maiusculo
print(curso.upper())

#minusculo
print(curso.lower())

#só a primeira maiuscula
print(curso.title())

#elimina espaço em branco
print(curso.strip())

#elimina o espaço em branco da esquerda
print(curso.lstrip())

#elimina o espaço em branco direita
print(curso.rstrip())

# JUNÇÃO E CENTRALIZAÇÃO

#centraliza e pode colocar a quantidade de caracteres que deverá ter
print(curso.center(10, "#"))

#adiciona os pontos entre as letras
print(".".join(curso))

